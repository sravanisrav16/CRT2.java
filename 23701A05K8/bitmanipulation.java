class bitmanipulation{
    public static void main(String[] args) {
        int a=5; //0101
        int b=3; //0011
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~a);
        System.out.println(10>>1);
        System.out.println(5<<2);
    }
}